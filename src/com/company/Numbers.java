// Displaying the numbers 1 to 4 on the same line
package com.company;

public class Numbers {

    public static void main(String[] args) {

       System.out.println("1 2 3 4");

       System.out.print("1 ");
       System.out.print("2 ");
       System.out.print("3 ");
       System.out.print("4");

       System.out.printf("%n%s%s%s%s","1 ","2 ","3 ","4 ");
    }
}
